    <script>
        // Fungsi untuk mengubah status dark mode
        function toggleDarkMode() {
            const body = document.body;
            const darkModeSwitch = document.getElementById('darkModeSwitch');

            // Toggle class dark-mode pada body
            body.classList.toggle('dark-mode');

            // Simpan preferensi pengguna ke localStorage
            const isDarkMode = body.classList.contains('dark-mode');
            localStorage.setItem('darkMode', isDarkMode);

            // Atur status switch sesuai dengan dark mode
            darkModeSwitch.checked = isDarkMode;
        }

        // Fungsi untuk memeriksa preferensi dark mode saat halaman dimuat
        function checkDarkModePreference() {
            const isDarkMode = localStorage.getItem('darkMode') === 'true';
            const body = document.body;
            const darkModeSwitch = document.getElementById('darkModeSwitch');

            // Terapkan dark mode jika preferensi pengguna ada
            if (isDarkMode) {
                body.classList.add('dark-mode');
            }

            // Atur status switch sesuai dengan dark mode
            darkModeSwitch.checked = isDarkMode;
        }

        // Jalankan fungsi checkDarkModePreference saat halaman dimuat
        document.addEventListener('DOMContentLoaded', checkDarkModePreference);

        // Tambahkan event listener untuk switch dark mode
        const darkModeSwitch = document.getElementById('darkModeSwitch');
        darkModeSwitch.addEventListener('change', toggleDarkMode);
    </script>
